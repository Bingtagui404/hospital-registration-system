#!/bin/bash

# 门诊挂号管理系统 - 停止脚本

echo "======================================"
echo "  停止门诊挂号管理系统..."
echo "======================================"

# 查找并终止进程
PID=$(lsof -t -i:8080)

if [ -n "$PID" ]; then
    kill -9 $PID
    echo "  已停止进程: $PID"
else
    echo "  未发现运行中的应用"
fi

echo "======================================"
