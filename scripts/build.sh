#!/bin/bash

# 门诊挂号管理系统 - 构建脚本

cd "$(dirname "$0")/.." || exit 1

echo "======================================"
echo "  开始构建项目..."
echo "======================================"

./mvnw clean package -DskipTests

if [ $? -eq 0 ]; then
    echo "======================================"
    echo "  构建成功！"
    echo "======================================"
else
    echo "======================================"
    echo "  构建失败！"
    echo "======================================"
    exit 1
fi
